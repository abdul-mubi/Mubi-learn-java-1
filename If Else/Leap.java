public class Leap {
    public static void main(String arg[]){
        int year=-500;
        if(year<=0){
            System.out.println("not a proper year");
        }
        else if(year%4==0)
        {
            System.out.println("the year is leap year");
        }
        else{
            System.out.println("the year is not a leap year");
        }

    }
}
    

