public class Vowel {
    public static void main(String arg[]) {
        char Y = 'a';
        if (Y == 'A' || Y == 'E' || Y == 'I' || Y == 'O' || Y == 'U') {
            System.out.println(Y);
        }

        else if (Y == 'a' || Y == 'e' || Y == 'i' || Y == 'o' || Y == 'u') {
            System.out.println(Y);
        }

        else {
            System.out.println("not a vowel");
        }

    }

}
