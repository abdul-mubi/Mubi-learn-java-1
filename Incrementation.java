public class Incrementation {
    public static void main (String a[]){

        int num = 5;
         num+=10;//num=num+10
         System.out.println(num);//15
         num-=7; //num=num-7
    
         System.out.println(num);//8
         num%=8;//num=num%8
         System.out.println(num);

         num/=2;
         System.out.println(num);

         num=10;
         System.out.println(num);
         
        int abd=20;
        abd=15;
        abd=75;
        System.out.println(abd); 
        
        int half =1;
        half+=2;
        half/=3;
        half-=1;
        System.out.println(half);   
    }
    
}
