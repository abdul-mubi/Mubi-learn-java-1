package whileloop;

public class demonestloop {
    public static void main(String arg[]) {
        int i = 1;
        while (i <= 4) {
            System.out.println("hi");
            int j = 1;
            while (j <= 3) {
                System.out.println("hello");
                j++;
            }
            i++;
        }

    }

}
